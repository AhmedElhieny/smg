let playerImg = document.querySelector(".playerImg img");
let quranName = document.querySelector(".quranName");
let theReader = document.querySelector(".theReader");
let quranAudio = document.querySelector("#quranAudio");
let playerPlay = document.querySelector(".playerPlay");
let next = document.querySelector("#next");
let prev = document.querySelector("#prev");
let repeat = document.querySelector("#repeat");
let progressBar = document.querySelector(".progressBar");
let playerProgress = document.querySelector(".playerProgress");
let startTime = document.querySelector(".startTime");
let maxTime = document.querySelector(".maxTime");
let PlayerImg = document.querySelector(".playerImg");

let quranIndex = 0;
let quranNotPlay = false;

window.addEventListener("load", () => {
  quranLoad(quranIndex);
});

function quranLoad(index) {
  playerImg.src = allQuranAudio[index].image;
  quranName.innerHTML = allQuranAudio[index].name;
  theReader.innerHTML = allQuranAudio[index].theReader;
  quranAudio.src = allQuranAudio[index].src;
}

// Play Quran
function quranPlay() {
  quranNotPlay = true;
  playerPlay.querySelector("i").innerHTML = "pause";
  PlayerImg.classList.add("play");
  quranAudio.play();
}

// Pause Quran
function quranPause() {
  quranNotPlay = false;
  playerPlay.querySelector("i").innerHTML = "play_arrow";
  PlayerImg.classList.remove("play");
  quranAudio.pause();
}

playerPlay.addEventListener("click", () => {
  quranNotPlay ? quranPause() : quranPlay();
});

// Next Quran
function nextQuran() {
  quranIndex++;
  if (quranIndex > allQuranAudio.length - 1) {
    quranIndex = 0;
  }
  quranLoad(quranIndex);
  quranPlay();
}

next.addEventListener("click", () => {
  nextQuran();
});

// Previous Quran
function prevQuran() {
  quranIndex--;
  if (quranIndex < 0) {
    quranIndex = allQuranAudio.length - 1;
  }
  quranLoad(quranIndex);
  quranPlay();
}

prev.addEventListener("click", () => {
  prevQuran();
});

repeat.addEventListener("click", () => {
  let getText = repeat.innerHTML;
  switch (getText) {
    case "repeat":
      repeat.innerHTML = "repeat_one";
      break;
    case "repeat_one":
      repeat.innerHTML = "repeat";
      break;
  }
});

quranAudio.addEventListener("ended", () => {
  let getText = repeat.innerHTML;
  switch (getText) {
    case "repeat":
      nextQuran();
      break;
    case "repeat_one":
      quranAudio.currentTime = 0;
      quranLoad(quranIndex);
      quranPlay();
      break;
  }
});

quranAudio.addEventListener("timeupdate", (event) => {
  let currentTime = event.target.currentTime;
  let durationTime = event.target.duration;
  if (!isNaN(durationTime)) {
    let quranTimeWidth = (currentTime / durationTime) * 100;
    progressBar.style.width = `${quranTimeWidth}%`;
  }
  startTime.innerHTML = formatTime(currentTime);
});

quranAudio.addEventListener("loadeddata", () => {
  let durationT = quranAudio.duration;
  maxTime.innerHTML = formatTime(durationT);
});

playerProgress.addEventListener("click", (e) => {
  let progressBarWidth = playerProgress.clientWidth;
  let progressBarX = e.offsetX;
  let quranDuration = quranAudio.duration;
  if (!isNaN(quranDuration)) {
    quranAudio.currentTime = (progressBarX / progressBarWidth) * quranDuration;
    quranPlay();
  }
});

function formatTime(time) {
  if (time && !isNaN(time)) {
    const minutes = Math.floor(time / 60) < 10 ? `0${Math.floor(time / 60)}` : Math.floor(time / 60);
    const seconds = Math.floor(time % 60) < 10 ? `0${Math.floor(time % 60)}` : Math.floor(time % 60);
    return `${minutes}:${seconds}`;
  }
  return "00:00";
}

let ul = document.querySelector(".ul");
for (let i = 0; i < allQuranAudio.length; i++) {
  let li = `
    <li li-index="${i}">
      <div class="row">
        <img style="width: 30px;" src="${allQuranAudio[i].image}" alt="">
        <div class="quranInfo">
          <span class="liname">${allQuranAudio[i].name}</span>
          <p>${allQuranAudio[i].theReader}</p>
        </div>
      </div>
      <audio class="audio-${i}" src="${allQuranAudio[i].src}"></audio>
      <i class="material-icons">more_horiz</i>
    </li>`;
  ul.insertAdjacentHTML("beforeend", li);
}

// Search Section
let searchInput = document.querySelector(".search input");
const allLiTag = ul.querySelectorAll("li");

searchInput.addEventListener("keyup", (key) => {
  let searchValue = key.target.value.toLowerCase();
  allLiTag.forEach((element) => {
    let text = element.querySelector(".liname").textContent.toLowerCase();
    if (text.includes(searchValue)) {
      element.style.display = "block";
    } else {
      element.style.display = "none";
    }
  });
});

for (let s = 0; s < allLiTag.length; s++) {
  allLiTag[s].setAttribute("onclick", "clicked(this)");
}

function clicked(item) {
  let getLiIndex = item.getAttribute("li-index");
  quranIndex = parseInt(getLiIndex);
  musicList.classList.remove("show");
  quranLoad(quranIndex);
  quranPlay();
}
